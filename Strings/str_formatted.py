# using % like c language
name = "Robert"
country = "India"
print("Welome %s to the %s"%(name, country))

# using format() function
name = "Robert"
country = "India"
print("Welome {0} to the {1}".format(name, country))


# using f-string
name = "Robert"
country = "India"
print(f"Welome {name} to the {country}")

a = 10
b = 20
print(f"sum of {a} and {b} is {a+b}")
print(f"product of {a} and {b} is {a*b}")